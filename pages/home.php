<p>Welcome to the nesemu2 website.</p>
<p>nesemu2 is an emulator for the NES/Famicom/Dendy.  It strives for perfection
  by favoring accuracy over speed.  nesemu2 should run at full speed on any
  modern computer.  It supports many mappers and has excellent compatability with
  just about any ROM image you encounter.
</p>